    private void popuniComboMesta() {
        try {
            List<Mesto> mesta = Kontroler.getInstance().vratiListuMesta();
            jcbMesto.setModel(new DefaultComboBoxModel(mesta.toArray()));
            Date dat = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            String datum = sdf.format(dat);
            jtfDatum.setText(datum);
            jtfDatum.setEditable(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }

    private AvioKompanijaPartner kreirajPartnera(String sSifra, String naziv, String ziro, String sDatum, String telefon, String email, String ulica, String broj, Mesto mesto) throws Exception {
        AvioKompanijaPartner partner = new AvioKompanijaPartner();

        if (sSifra == null || sSifra.isEmpty()) {
            throw new Exception("Sifra nije uneta!!!");
        }
        try {
            int sifra = Integer.parseInt(sSifra);
            partner.setSifraAVK(sifra);
        } catch (Exception e) {
            throw new Exception("Sifru treba uneti u numerickom formatu!!!");
        }
        if (naziv == null || naziv.isEmpty()) {
            throw new Exception("Naziv kompanije nije unet!!!");
        }
        partner.setNaziv(naziv);
        if (ziro == null || ziro.isEmpty()) {
            throw new Exception("Ziro-racun kompanije nije unet!!!");
        }
        if (ziro.length() != 18) {
            throw new Exception("Ziro-racun kompanije nije unet u odgovarajucem formatu!!!");
        }
        partner.setZiroRacun(ziro);
        if (sDatum == null || sDatum.isEmpty()) {
            throw new Exception("Datum sklapanja partnerstva sa kompanijom nije unet!!!");
        }
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            Date dat = sdf.parse(sDatum);
            partner.setDatumSklapanjaPartnerstva(dat);
        } catch (Exception e) {
            throw new Exception("Datum sklapanja partnerstva sa kompanijom nije unet u odgovarajucem formatu!!!");
        }
        if (telefon == null || telefon.isEmpty()) {
            throw new Exception("Telefon kompanije nije unet!!!");
        }
        partner.setTelefon(telefon);
        if (email == null || email.isEmpty()) {
            throw new Exception("E-mail adresa kompanije nije uneta!!!");
        }
//        if (!email.contains("@")) {
//            throw new Exception("E-mail adresa kompanije nije uneta u odgovarajucem formatu!!!");
//        }
        partner.setEmail(email);
        if (ulica == null || ulica.isEmpty()) {
            throw new Exception("Ulica u kojoj se nalazi kompanija nije uneta!!!");
        }
        partner.setUlica(ulica);
        if (broj == null || broj.isEmpty()) {
            throw new Exception("Broj u ulici u kojoj se nalazi kompanija nije unet!!!");
        }
        partner.setBroj(broj);
        partner.setMesto(mesto);
        return partner;
    }

