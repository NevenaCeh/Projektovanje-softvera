    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        // TODO add your handling code here:
        int odluka = JOptionPane.showConfirmDialog(this, "Da li zelite da sacuvate ovog partnera?");
        if (odluka == JOptionPane.YES_OPTION) {
            String sSifra = jtfSifraAvk.getText().trim();
            String naziv = jtfNazivAvk.getText().trim();
            String ziro = jtfZiroRn.getText().trim();
            String sDatum = jtfDatum.getText().trim();
            String telefon = jtfTelefon.getText().trim();
            String email = jtfMejl.getText().trim();
            String ulica = jtfUlica.getText().trim();
            String broj = jtfBroj.getText().trim();
            Mesto mesto = (Mesto) jcbMesto.getSelectedItem();
            try {
                AvioKompanijaPartner partner = kreirajPartnera(sSifra, naziv, ziro, sDatum, telefon, email, ulica, broj, mesto);
                Kontroler.getInstance().unesiPartneraUBazu(partner);
                ServerTransferObjekat sto = (ServerTransferObjekat) Kontroler.getInstance().getMapa().get("sacuvanPartner");
                if (sto.getStatus() == Util.SERVER_STATUS_OPERACIJA_OK) {
                    JOptionPane.showMessageDialog(this, sto.getRezultat().toString());
                } else {
                    JOptionPane.showMessageDialog(this, sto.getGreska());
                }
               // JOptionPane.showMessageDialog(this, "Partner je sacuvan u bazi!!!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }

    }//GEN-LAST:event_jbtnSacuvajActionPerformed

