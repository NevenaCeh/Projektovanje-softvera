    private void proveraLogin(String usn, String lozinka) throws Exception {
        if (usn == null || usn.isEmpty()) {
            throw new Exception("Niste uneli username!");
        }
        if (lozinka == null || lozinka.isEmpty()) {
            throw new Exception("Niste uneli password!");
        }
    }
