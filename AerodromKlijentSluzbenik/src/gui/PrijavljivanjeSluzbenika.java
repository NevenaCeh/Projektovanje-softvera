    private void jbtnPrijavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPrijavaActionPerformed
        // TODO add your handling code here:
        String usn = jtfUsername.getText().trim();
        String lozinka = "";
        char[] slova = jpfPassword.getPassword();
        for (char s : slova) {
            lozinka += s;
        }
        try {
            proveraLogin(usn, lozinka);
            Kontroler.getInstance().prijaviKorisnika(usn, lozinka, this);
            JOptionPane.showMessageDialog(this, "Uspesno ste se prijavili na sistem");
            jtfUsername.setText("");
            jpfPassword.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jbtnPrijavaActionPerformed

